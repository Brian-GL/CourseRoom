    public static String Convertir_Segundos(long seconds) {
        long segundos_Absolutos = Math.abs(seconds);
        String valor_Positivo = String.format(
                "%02d:%02d:%02d",
                segundos_Absolutos / 3600,
                (segundos_Absolutos % 3600) / 60,
                segundos_Absolutos % 60);
        return seconds < 0 ? MainFrame.Concatenar("-",valor_Positivo) : valor_Positivo;
    }
    
