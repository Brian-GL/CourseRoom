    private void editar_Imagen_Grupo_JButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editar_Imagen_Grupo_JButtonMouseClicked
        // TODO add your handling code here:
        if(SwingUtilities.isLeftMouseButton(evt)){

            JFileChooser escogedor_Archivo = new JFileChooser();
            escogedor_Archivo.setAccessory(new ImageFilePreview(escogedor_Archivo));
            FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos De Imagenes", "png", "jpg", "jpeg", "bmp");
            escogedor_Archivo.addChoosableFileFilter(filtro);
            escogedor_Archivo.setFileSelectionMode(JFileChooser.FILES_ONLY);
            escogedor_Archivo.setAcceptAllFileFilterUsed(true);
            escogedor_Archivo.setApproveButtonText("Cargar Imagen");
            int resultado = escogedor_Archivo.showOpenDialog(this);

            if (resultado == JFileChooser.APPROVE_OPTION) {
                File archivo = escogedor_Archivo.getSelectedFile();

                try {
                    Image abrir_Imagen = ImageIO.read(archivo);
                    int largo_Imagen = editar_Imagen_Grupo_JLabel.getHeight();
                    ImageIcon autenticacionIcon = new ImageIcon(abrir_Imagen.getScaledInstance(largo_Imagen,largo_Imagen,Image.SCALE_SMOOTH));

                    editar_Imagen_Grupo_JLabel.setIcon(autenticacionIcon);

                    Student_Dashboard_Panel.setUserImage(autenticacionIcon.getImage());
                    abrir_Imagen.flush();

                } catch (IOException ex) {
                    MainFrame.getLogger().log(Level.SEVERE, null, ex);
                }
            }

        }
    }//GEN-LAST:event_editar_Imagen_Grupo_JButtonMouseClicked

