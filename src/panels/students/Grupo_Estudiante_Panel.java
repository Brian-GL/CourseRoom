    private void Enviar_Mensaje(){
        String mensaje = enviar_Mensaje_JTextField.getText();
        if(!mensaje.isEmpty() && !mensaje.isBlank()){
            
            String emisor = MainFrame.getFaker().dune().character();
            String fecha = MainFrame.getFaker().backToTheFuture().date();
            Mensaje_Texto_General_Panel mensaje_Texto_General_Panel = new Mensaje_Texto_General_Panel(emisor,mensaje,fecha);
            mensaje_Texto_General_Panel.setMaximumSize(new Dimension(Toolkit.getDefaultToolkit().getScreenSize().width-400,200));
            mensajes_Chat_JPanel.add(mensaje_Texto_General_Panel);
            enviar_Mensaje_JTextField.setText("");
                    
        }
    }
    
    private void Compartir_Archivo(){
        JFileChooser escogedor_Archivo = new JFileChooser();
        escogedor_Archivo.setFileSelectionMode(JFileChooser.FILES_ONLY);
        escogedor_Archivo.setApproveButtonText("Compartir Archivo");
        escogedor_Archivo.setMultiSelectionEnabled(false);
        escogedor_Archivo.doLayout();
        int result = escogedor_Archivo.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File archivo = escogedor_Archivo.getSelectedFile();
            if(archivo != null){
                
                Date fecha = MainFrame.getFaker().date().birthday();
                Archivo_Compartido_Grupo_Estudiante_Panel archivo_Compartido_Grupo_Estudiante_Panel = 
                        new Archivo_Compartido_Grupo_Estudiante_Panel(archivo,"Nombre Del Usuario",
                        fecha.toString(),segundo_Color,segundo_Color_Fuente,tercer_Color,
                        tercer_Color_Fuente,primer_Color_Fuente);
                
                archivos_Compartidos_JPanel.add(archivo_Compartido_Grupo_Estudiante_Panel);

            }
        }
    }
    

