    private String toRGB(Color color){
        
        
        return MainFrame.Concatenate(
                MainFrame.Concatenate(
                MainFrame.Concatenate(
                MainFrame.Concatenate(
                MainFrame.Concatenate(
                        MainFrame.Concatenate(
                                "RGB: (", color.getRed()), ","),color.getGreen()),","),color.getBlue()),")");
        
    }

