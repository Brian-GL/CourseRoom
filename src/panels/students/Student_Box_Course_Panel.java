    @Override
    public void setColors(Image image){
        
        try {
            Random colorRandom = new Random(System.currentTimeMillis());
            int maximum = 0;
            firstColor = Color.BLACK;
            PairDoublyLinkedList<Integer, Color> colorList = new PairDoublyLinkedList<>();
            PixelGrabber pg = new PixelGrabber(image, 0, 0, -1, -1, false);
            int large = image.getWidth(null)/2;
            int pixel;
            int red;
            int green;
            int blue;
            Color color;
            Pair<Integer, Color> pair;
            int number;
            int position;
            int[] pixels;
            if (pg.grabPixels()) {
                pixels = (int[]) pg.getPixels();
                for(int i = 0; i < pixels.length; i++){
                    pixel = pixels[i];
                    red = (pixel  & 0x00ff0000) >> 16;
                    green = (pixel & 0x0000ff00) >> 8;
                    blue = pixel & 0x000000ff;
                    color = new Color(red,green,blue);
                    pair = colorList.get_from_second(color);
            
                    if (pair != null) {//exist
                        number = pair.first()+ 1;
                        pair.first(number);
                        if (number > maximum) {
                            firstColor = color;
                            maximum = number;
                        }
                    } else {
                        colorList.push_back(1, color);
                    }

                    i += colorRandom.nextInt(large+1) + large;
                }
                
                
                secondColor = firstColor;
            
                int iterations = 0;
                if(colorList.size() > 1){
                    
                    while(Math.abs(secondColor.getRGB() - firstColor.getRGB()) < 3000000){
                        position = colorRandom.nextInt((int)colorList.size()-1);
                        secondColor = colorList.get(position).second();
                        iterations++;
                        if(iterations > 25){
                             while(firstColor.getRGB() == secondColor.getRGB()){
                                position = colorRandom.nextInt((int)colorList.size()-1);
                                secondColor = colorList.get(position).second();
                            }
                             break;
                        }
                    }
                }

                
                red = firstColor.getRed();
                fontColor = (red >= 155) ? Color.BLACK : Color.WHITE;
                red = secondColor.getRed();
                secondFontColor = (red >= 155) ? Color.BLACK : Color.WHITE;
               
                colorList.clear();

                jLabelCourseInformation.setForeground(fontColor);
                
                jTextPaneCourseRating.setForeground(fontColor);
                jTextPaneCourseRating.setBackground(firstColor);
                jScrollPaneCourseRating.setBackground(firstColor);
                
                jLabelCourseName.setForeground(fontColor);
                jLabelTeacherName.setForeground(fontColor);
                jLabelStudentsNumber.setForeground(fontColor);
                
                jButtonReviews.setForeground(secondFontColor);
                jButtonReviews.setBackground(secondColor);
                
                jPanelCourseInformation.setBackground(firstColor);
                jPanelCourseInformation.setBorder(javax.swing.BorderFactory.createLineBorder(firstColor));
                
            }
            
        } catch (InterruptedException ex) {
            MainFrame.getLogger().log(Level.SEVERE, null, ex);
        }
    }

