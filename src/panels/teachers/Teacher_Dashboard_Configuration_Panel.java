    private void jButtonSelectDownloadRouteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSelectDownloadRouteMouseClicked
        // TODO add your handling code here:
        if(SwingUtilities.isLeftMouseButton(evt)){
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            fileChooser.setApproveButtonText("Open Music Folder");
            int result = fileChooser.showOpenDialog(this);

            if (result == JFileChooser.APPROVE_OPTION) {
                File folder = fileChooser.getSelectedFile();
                if(folder != null){
                    
                    jTextFieldDownloadRoute.setText(folder.getAbsolutePath());
                
                }
                
                folder = null;
            }
        }
    }//GEN-LAST:event_jButtonSelectDownloadRouteMouseClicked

