    private void jButtonCargarImagenPerfilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCargarImagenPerfilMouseClicked
        // TODO add your handling code here:
        if(SwingUtilities.isLeftMouseButton(evt)){
            
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setAccessory(new ImageFilePreview(fileChooser));
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos De Imagenes", "png", "jpg", "jpeg", "bmp");
            fileChooser.addChoosableFileFilter(filter);
            fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
            fileChooser.setAcceptAllFileFilterUsed(true);
            fileChooser.setApproveButtonText("Cargar Imagen");
            int result = fileChooser.showOpenDialog(this);

             if (result == JFileChooser.APPROVE_OPTION) {
                 File file = fileChooser.getSelectedFile();
                 
                try {
                    Image openImage = ImageIO.read(file);
                    int large = jLabelImagenPerfilAutenticacion.getHeight();
                    Image scaled = Teacher_Dashboard_Panel.getUserImage().getScaledInstance(large, large, Image.SCALE_SMOOTH);
                    ImageIcon autenticacionIcon = new ImageIcon(scaled);
                    
                    jLabelImagenPerfilAutenticacion.setIcon(autenticacionIcon);
                    
                    Teacher_Dashboard_Panel.setUserImage(openImage);
                    
                    JOptionPane.showMessageDialog(null, "Los Cambios En La Interfaz Se Verán Reflejados \nEn Su Próximo Inicio De Sesión", "Información", JOptionPane.INFORMATION_MESSAGE);
                    
                    openImage.flush();
                    scaled.flush();
                    scaled = null;
                    autenticacionIcon = null;
                    
                    
                } catch (IOException ex) {
                    Logger.getLogger(Teacher_Edit_Profile_Panel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
             
            fileChooser = null;
        }
    }//GEN-LAST:event_jButtonCargarImagenPerfilMouseClicked

