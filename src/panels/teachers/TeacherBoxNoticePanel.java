            ImageIcon avisoIcon = new ImageIcon(avisoImage);
            jLabelFotoAviso.setIcon(avisoIcon);;
            getImage.flush();
            getImage = null;
            avisoIcon = null;
            avisoImage.flush();
            avisoImage = null;
            imageURL = null;
            faker = null;
            mx = null;
