    /**Sets all available presets*/
    public void setPresets(){
        
        Iterator<Entry<String,Equalizer>> iterator = presets_map.entrySet().iterator();
        
        while(iterator.hasNext()){
            Entry<String,Equalizer> entry = iterator.next();
            jComboBoxPresets.addItem(entry.getKey());
        }
        
        jComboBoxPresets.setSelectedItem("Flat");
    }
    
