    /**Charge Metadata On Screen*/
    private void loadMetadata(){
        try {
            //File readFile = new File(actual_node.element());
            File readFile = openFiles[index];
            
            if(readFile.exists()){      
               
                AudioFile audioFile = AudioFileIO.read(readFile);
                
                Tag tag = audioFile.getTag();
                String title = tag.getFirst(FieldKey.TITLE);
                String artist = tag.getFirst(FieldKey.ARTIST);
                String album = tag.getFirst(FieldKey.ALBUM);
                String genre = tag.getFirst(FieldKey.GENRE);
                String year = tag.getFirst(FieldKey.YEAR);
                String media_info = audioFile.getExt().toUpperCase() + " File - " + audioFile.getAudioHeader().getBitRate() +" kbps";
                String lyricals = tag.getFirst(FieldKey.LYRICS);
                if(title == null){
                    title = readFile.getName();
                }
                if(title.isEmpty()){
                    title = readFile.getName();
                }
                jLabelTitle.setToolTipText("Title: "+title);
                jLabelArtist.setToolTipText("Artist: "+artist);
                jLabelAlbum.setToolTipText("Album: "+album);

                jLabelTitle.setText(title);
                jLabelArtist.setText(artist);
                jLabelAlbum.setText(album);
                
                jLabelGenreValue.setText(genre);
                jLabelYearValue.setText(year);
                jLabelMediaValue.setText(media_info);
                jTextPaneLyrics.setText(lyricals);

                Artwork artWork = tag.getFirstArtwork();
                Image playlistEntryImage = null;
                if(artWork != null){
                    
                    int large = jLabelCoverArt.getHeight();
                    Image real_cover = ((Image)artWork.getImage());
                    if(real_cover != null){
                        Image coverImagen = real_cover.getScaledInstance(large, large, Image.SCALE_SMOOTH);
                        playlistEntryImage = real_cover.getScaledInstance(63, 63, Image.SCALE_SMOOTH);
                        ImageIcon cover = new ImageIcon(coverImagen);
                        jLabelCoverArt.setIcon(cover);
                        setColors(coverImagen);
                        coverImagen.flush();
                        coverImagen = null;
                        real_cover.flush();
                        real_cover = null;
                        cover = null;
                        
                    }
                } 
                PlaylistEntryPanel playlistEntryPanel = (PlaylistEntryPanel)jPanelPlaylist.getComponent(index);
                if(playlistEntryPanel.isNotSetted()){
                    playlistEntryPanel.setTitle(title);
                    playlistEntryPanel.setArtist(artist);
                    playlistEntryPanel.setAlbum(album);
                    if(playlistEntryImage != null){
                        playlistEntryPanel.setCoverArt(playlistEntryImage);
                    }
                }
               
                playlistEntryPanel = null;
                media_info = null;
                playlistEntryImage = null;
                audioFile = null;
                tag = null;
                title = null;
                artist = null;
                album = null;
                artWork = null;
                genre = null;
                year = null;
                lyricals = null;
            }else{
                String mrl = readFile.getAbsolutePath();
                JOptionPane.showMessageDialog(this, mrl,"UNABLE TO FIND MEDIA",JOptionPane.ERROR_MESSAGE);
                audio_list_player_component.mediaListPlayer().list().media().remove(index);
                if(openFiles != null){
                    int aux_index = 0;
                    File[] aux = new File[audio_list_player_component.mediaListPlayer().list().media().count()];
                    for(int i = 0; i < openFiles.length;i++){
                        if(i != index){
                            aux[aux_index] = openFiles[i];
                            aux_index++;
                        }
                    }
                }
                if((index+1) < audio_list_player_component.mediaListPlayer().list().media().count()){
                    moveNext();
                } else if((index - 1) >= 0){
                    movePrevious();
                } else{
                    setStop();
                }
            }
            readFile = null;
        } catch (CannotReadException | IOException | TagException | ReadOnlyFileException | InvalidAudioFrameException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Found Error",JOptionPane.ERROR_MESSAGE);
        }
        
        System.gc();

    }
    
    private String secondsToString(long seconds) {
        long absSeconds = Math.abs(seconds);
        String positive = String.format(
                "%02d:%02d:%02d",
                absSeconds / 3600,
                (absSeconds % 3600) / 60,
                absSeconds % 60);
        return seconds < 0 ? "-" + positive : positive;
    }
    
    @Override
    public void setColors(Image image) {
        try {
            
            int maximum = 0;
            PairDoublyLinkedList<Integer, Color> colorList = new PairDoublyLinkedList<>();
            PixelGrabber pg = new PixelGrabber(image, 0, 0, -1, -1, false);
            Random colorRandom = new Random(System.currentTimeMillis());
            Color firstColor = Color.BLACK;
            int large = image.getHeight(null)/2;
            if (pg.grabPixels()) {
                int[] pixels = (int[]) pg.getPixels();
                for(int i = 0; i < pixels.length; i++){
                    int pixel = pixels[i];
                    int  red = (pixel  & 0x00ff0000) >> 16;
                    int  green = (pixel & 0x0000ff00) >> 8;
                    int  blue = pixel & 0x000000ff;
                    Color color = new Color(red,green,blue);
                    Pair<Integer, Color> pair = colorList.get_from_second(color);
            
                    if (pair != null) {//exist
                        int number = pair.first()+ 1;
                        pair.first(number);
                        if (number > maximum) {
                            firstColor = color;
                            maximum = number;
                        }
                    } else {
                        colorList.push_back(1, color);
                    }

                    i+= colorRandom.nextInt(large+1) + large;
                }

                
                int red = firstColor.getRed();
                Color fontColor = (red >= 155) ? Color.BLACK : Color.WHITE;

                colorList.clear();

                
                Component[] components = this.getComponents();

                for (Component component : components){
                    component.setForeground(fontColor);
                }

                jSliderRate.setForeground(fontColor);
                jSliderRate.setForeground(fontColor);
                jLabelTitle.setForeground(fontColor);
                jLabelAlbum.setForeground(fontColor);
                jLabelArtist.setForeground(fontColor);
                jLabelProgress.setForeground(fontColor);
                jLabelTotalDuration.setForeground(fontColor);
                jSliderProgress.setForeground(fontColor);
                jLabelCoverArt.setForeground(fontColor);
                
               
                components = jPanelMediaInformation.getComponents();

                for (Component component : components){
                    component.setForeground(fontColor);
                }
                
                components = jPanelEqualizer.getComponents();

                for (Component component : components){
                    component.setForeground(fontColor);
                }
                
                components = jPanelBands.getComponents();

                for (Component component : components){
                    component.setForeground(fontColor);
                    component.setBackground(fontColor);
                }
                
                components = jPanelPlaylist.getComponents();
                
                for (Component component : components){
                    PlaylistEntryPanel playlistEntryPanel = (PlaylistEntryPanel)component;
                    playlistEntryPanel.setForegrounds(fontColor);
                    
                }
               
                TitledBorder border = (TitledBorder)jLabelGenreValue.getBorder();
                border.setTitleColor(fontColor);

                border = (TitledBorder)jLabelYearValue.getBorder();
                border.setTitleColor(fontColor);

                border = (TitledBorder)jLabelMediaValue.getBorder();
                border.setTitleColor(fontColor);

                border = (TitledBorder)jTextPaneLyrics.getBorder();
                border.setTitleColor(fontColor);
                
                border = (TitledBorder)jPanelBands.getBorder();
                border.setTitleColor(fontColor);
        
                jComboBoxPresets.setForeground(fontColor);
                jComboBoxPresets.setBackground(firstColor);
                jTextPaneLyrics.setForeground(fontColor);
                this.setBackground(firstColor);
                
                firstColor = null;
                colorList = null;
                fontColor = null;
                pg = null;
                pixels = null;
                colorRandom = null;
            }
            
        } catch (InterruptedException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Found Error",JOptionPane.ERROR_MESSAGE);
        }
        
        System.gc();
    }
        

