    private void jLabelOpenFolderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelOpenFolderMouseClicked
        // TODO add your handling code here:

        if(SwingUtilities.isLeftMouseButton(evt)){

            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            fileChooser.setApproveButtonText("Open Music Folder");
            int result = fileChooser.showOpenDialog(this);

            if (result == JFileChooser.APPROVE_OPTION) {
                File folder = fileChooser.getSelectedFile();
                if(folder != null){
                    FileFilter filter = (File pathname) -> {
                        if(pathname != null){
                            return pathname.getName().endsWith(".mp3") ||
                                    pathname.getName().endsWith(".flac") ||
                                    pathname.getName().endsWith(".aac") ||
                                    pathname.getName().endsWith(".wma") ||
                                    pathname.getName().endsWith(".ogg");
                        }
                        return false;
                    };

                   File[] files = folder.listFiles(filter);
                    if(files.length > 0){
                        zero_index = false;
                        moving_next = false;
                        moving_previous = true;
                        audio_list_player_component.mediaListPlayer().controls().stop();
                        audio_list_player_component.mediaListPlayer().list().media().clear();
                        jPanelPlaylist.removeAll();
                        int idx = 0;
                        for (File file : files){
                            @SuppressWarnings("null")
                            String path = file.getAbsolutePath();
                            String filename = file.getName();
                            PlaylistEntryPanel playlistEntryPanel = new PlaylistEntryPanel(filename,idx);
                            jPanelPlaylist.add(playlistEntryPanel);
                            audio_list_player_component.mediaListPlayer().list().media().add(path);
                            filename = null;
                            path = null;
                            playlistEntryPanel = null;
                            idx++;
                            file = null;
                        }
                        index = 0;
                        openFiles = files;
                        loadMetadata();
                        audio_list_player_component.mediaListPlayer().controls().play();
                        jSliderVolume.setEnabled(true);
                        jSliderVolume.setValue(100);
                        jSliderRate.setEnabled(true);
                        jSliderVolume.setValue(100);
                        jLabelVolumeUp.setEnabled(true);
                        jLabelVolumeDown.setEnabled(true);
                        jLabelRateUp.setEnabled(true);
                        jLabelRateDown.setEnabled(true);
                        jLabelNext.setEnabled(true);
                        jLabelPrevious.setEnabled(true);
                        jLabelStop.setEnabled(true);
                        jLabelPlayPause.setEnabled(true);
                        jSliderProgress.setEnabled(true);
                        jPanelBands.setEnabled(true);
                        jSliderBandPreamp.setEnabled(true);
                        jComboBoxPresets.setEnabled(true);
                    }
                    //files = null;
                    filter = null;
                }
                
                folder = null;
            }

            fileChooser = null;
        }
        
        System.gc();
    }//GEN-LAST:event_jLabelOpenFolderMouseClicked

