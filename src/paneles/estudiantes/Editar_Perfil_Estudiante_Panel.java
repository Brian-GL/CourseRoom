    private void imagen_Cambiar_JButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imagen_Cambiar_JButtonMouseClicked
        // TODO add your handling code here:
        if(SwingUtilities.isLeftMouseButton(evt)){
            
            JFileChooser escogedor_Archivo = new JFileChooser();
            FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos De Imagen", "png", "jpg", "jpeg", "bmp");
            escogedor_Archivo.addChoosableFileFilter(filtro);
            escogedor_Archivo.setFileSelectionMode(JFileChooser.FILES_ONLY);
            escogedor_Archivo.setAcceptAllFileFilterUsed(true);
            escogedor_Archivo.setApproveButtonText("Cargar Imagen");
            int result = escogedor_Archivo.showOpenDialog(this);

             if (result == JFileChooser.APPROVE_OPTION) {
                File archivo_Abierto = escogedor_Archivo.getSelectedFile();
                
                if(archivo_Abierto != null){
                    try {
                        
                        Image obtener_Imagen = ImageIO.read(archivo_Abierto);
                        int largo_Imagen = imagen_Perfil_JLabel.getHeight();
                        Image imagen_Escalada = Tablero_Estudiante_Panel.Imagen_Usuario().getScaledInstance(largo_Imagen, largo_Imagen, Image.SCALE_SMOOTH);
                        ImageIcon icono_Imagen = new ImageIcon(imagen_Escalada);

                        imagen_Perfil_JLabel.setIcon(icono_Imagen);

                        Tablero_Estudiante_Panel.Imagen_Usuario(obtener_Imagen);

                        JOptionPane.showMessageDialog(null, "Los Cambios En La Interfaz Se Verán Reflejados \nEn Su Próximo Inicio De Sesión", "Información", JOptionPane.INFORMATION_MESSAGE);

                        obtener_Imagen.flush();
                        imagen_Escalada.flush();

                    } catch (IOException ex) {
                        
                    }
                }
            }
             
        }
    }//GEN-LAST:event_imagen_Cambiar_JButtonMouseClicked

