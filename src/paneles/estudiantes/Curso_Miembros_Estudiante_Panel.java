    @Override
    public void Iniciar_Componentes() {
        miembros_Curso_JScrollPane.getViewport().setOpaque(false);
        miembros_Curso_JScrollPane.getVerticalScrollBar().setUnitIncrement(15);
        miembros_Curso_JScrollPane.getHorizontalScrollBar().setUnitIncrement(15);

        Font gadugi = new Font("Gadugi", Font.BOLD, 16);
        miembros_Curso_JTable.getTableHeader().setFont(gadugi);

        miembros_Curso_JTable.setDefaultRenderer(Celda_Renderer.class, new Celda_Renderer());
        Celda_Renderer[] celdas = new Celda_Renderer[4];
        DefaultTableModel modelo = (DefaultTableModel) miembros_Curso_JTable.getModel();

        String id;
        URL url_Imagen;
        Image obtener_Imagen = null;
        ImageIcon icono_Chat = null;
        var icono_Chatear = new ImageIcon(getClass().getResource("/recursos/iconos/online-chat.png"));
        for (int i = 0; i < CourseRoom.Faker().number().numberBetween(1, 5); i++) {
            
            id = CourseRoom.Concatenar("Chat_", i+Pagina_Chats_Estudiante_Panel.Numero_Chats());
            try {
                System.out.println(id + " -> Getting Image From https://i.pravatar.cc/96");
                url_Imagen = new URL("https://i.pravatar.cc/96");
                obtener_Imagen = ImageIO.read(url_Imagen);
                icono_Chat = new ImageIcon(obtener_Imagen);

                celdas[0] = new Celda_Renderer(icono_Chat, CourseRoom.Faker().name().fullName(), id);
                celdas[1] = new Celda_Renderer(CourseRoom.Faker().name().username(), id);
                celdas[2] = new Celda_Renderer(CourseRoom.Faker().date().birthday(0, 1).toString(), id);
                celdas[3] = new Celda_Renderer(icono_Chatear, id);

                Pagina_Chats_Estudiante_Panel.Agregar_Chat(celdas[0], celdas[2], id);
                modelo.addRow(celdas);

                obtener_Imagen.flush();
                obtener_Imagen.getGraphics().dispose();
            } catch (MalformedURLException ex) {

            } catch (IOException ex) {

            }
        }

        miembros_Curso_JTable.addMouseListener(new MouseAdapter() {

            @Override
            public void mousePressed(MouseEvent e) {
                if (e.getClickCount() == 2) {

                    JTable tabla = (JTable) e.getComponent();
                    int fila = tabla.getRowSorter().convertRowIndexToModel(tabla.getSelectedRow());
                    int columna = tabla.getSelectedColumn();

                    //Mostrar Chat
                    if (columna == 3){
                        DefaultTableModel modelo = (DefaultTableModel) miembros_Curso_JTable.getModel();

                        Celda_Renderer celda = (Celda_Renderer) modelo.getValueAt(fila, columna);

                        Tablero_Estudiante_Panel.Mostrar_Vista(celda.ID());
                    }

                }
            }
        });

    }
    
    @Override
    public void Colorear_Componentes() {
        contenido_Titulo_JPanel.setBackground(segundo_Color);

        titulo_JLabel.setBackground(tercer_Color);
        titulo_JLabel.setForeground(tercer_Color_Fuente);
        
        miembros_Curso_JTable.getTableHeader().setBackground(segundo_Color);
        miembros_Curso_JTable.getTableHeader().setForeground(segundo_Color_Fuente);
        
        DefaultTableModel modelo = (DefaultTableModel) miembros_Curso_JTable.getModel();
        Celda_Renderer celda;
        for (int i = 0; i < miembros_Curso_JTable.getRowCount(); i++) {
            for (int j = 0; j < 4; j++) {
                celda = (Celda_Renderer) modelo.getValueAt(i, j);
                celda.Color_Fuente(primer_Color_Fuente);
            }
        }

    }

    @Override
    public void Limpiar() {
        miembros_Curso_JTable.removeAll();
    }

